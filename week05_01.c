#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main() {
	int a;

	// a 입력
	printf("a의 값을 입력하세요: ");
	scanf("%d", &a);

	// 결과 출력
	printf("%d를 7로 나눈 몫은 5이다. : %d\n", a, a / 7 == 5);
	printf("%d를 3으로 나눈 나머지는 0이다. : %d\n", a, a % 3 == 0);
	printf("%d는 5의 배수가 아니다. : %d\n", a, a % 5 != 0);
	printf("%d는 3의 배수이고, 짝수이다. : %d\n", a, (a % 3 == 0) && (a % 2 == 0));
	printf("%d는 4또는 7로 나누면 나머지가 0이다. : %d", a, (a % 4 == 0) || (a % 7 == 0));

	return 0;
}